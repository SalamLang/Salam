const char *i18n_tr(const char *msgid)
{
    const char *lang;
    const i18n_pair_t *p = NULL;
    if (!msgid) return msgid;
    lang = i18n_active_lang();
    if (strcmp(lang, "fa") == 0)
        p = k_msg_fa;
    else if (strcmp(lang, "ar") == 0)
        p = k_msg_ar;
    if (p) {
        for (; p->key; p++)
            if (strcmp(p->key, msgid) == 0) return p->val;
    }
    return msgid;
}
