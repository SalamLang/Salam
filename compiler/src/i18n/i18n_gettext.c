const char *i18n_tr(const char *msgid)
{
    const char *lang;
    const i18n_msg_t *p;
    bool fa, ar;
    if (!msgid) return msgid;
    lang = i18n_active_lang();
    fa = strcmp(lang, "fa") == 0;
    ar = strcmp(lang, "ar") == 0;
    if (!fa && !ar) return msgid;
    for (p = k_msgs; p->en; p++)
        if (strcmp(p->en, msgid) == 0) {
            const char *tr = fa ? p->fa : p->ar;
            return tr ? tr : msgid;
        }
    return msgid;
}
